# (+)  ->  Toplama
# (-)  ->  Çıkarma
# (*)  ->  Çarpma
# (/)  ->  Bölme
# (**) ->  Üs Alma
# (%)  ->  Mod
# (//) -> Bölme [Ondalık kısmını atar ve aşağı yuvarlar.]
print(10+2)     # 12
print(10.2+2)   # 12.2
print(12-2)     # 10
print(2.2-2)    # 0.20000000000000018
print(2*3)      # 6
print(2.5*3)    # 7.5
print(15/4)     # 3.75
print(5.8/2)    # 2.9
print(5**2)     # 25
print(10%3)     # 1
print(15//4)    # 3
print(-15//4)   # -4